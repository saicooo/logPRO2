% ============================================================
% Задание 1. Списки, вариант 3.
% Проверить, является ли заданный список "палиндромным" (симметричным)
%
% Пример:
% ?- palind_list([1,2,3,4,5,4,3,2,1]).
% Yes
% ?- palind_list([1,2,3]).
% No
% ============================================================

% Вспомогательный предикат: разворот списка
reverse_list([], Acc, Acc).
reverse_list([H|T], Acc, Rev) :-
    reverse_list(T, [H|Acc], Rev).

% Основной предикат палиндрома: список равен своему развороту
palind_list(L) :-
    reverse_list(L, [], Rev),
    L = Rev.

% ============================================================
% Задание 2. Деревья, вариант 3.
% Проверить, является ли заданное бинарное дерево двоичным справочником
% (в каждом узле - в левом поддереве - все элементы, меньшие узлового,
%  в правом - большие либо равные узловому)
%
% Дерево представляется как: tr(Value, Left, Right) или nil
%
% Пример:
% ?- isBinaryDict(tr(5, tr(4, nil, nil), tr(8, tr(6, tr(3, nil, nil), nil), tr(9, nil, nil)))).
% No
% ?- isBinaryDict(tr(5, tr(3, nil, nil), tr(8, nil, nil))).
% Yes
% ============================================================

% Все элементы дерева меньше заданного значения
all_less(nil, _).
all_less(tr(V, L, R), Max) :-
    V < Max,
    all_less(L, Max),
    all_less(R, Max).

% Все элементы дерева больше или равны заданному значению
all_geq(nil, _).
all_geq(tr(V, L, R), Min) :-
    V >= Min,
    all_geq(L, Min),
    all_geq(R, Min).

% Проверка бинарного справочника (BST):
% nil - пустое дерево является BST
isBinaryDict(nil).
% Непустое дерево является BST, если:
% - все элементы левого поддерева меньше корня
% - все элементы правого поддерева >= корня
% - оба поддерева тоже являются BST
isBinaryDict(tr(V, L, R)) :-
    all_less(L, V),
    all_geq(R, V),
    isBinaryDict(L),
    isBinaryDict(R).
